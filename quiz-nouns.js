const vibhaktis = ["प्रथमा", "द्वितीया", "तृतीया", "चतुर्थी", "पञ्चमी", "षष्ठी", "सप्तमी"];
const vacanas = ["एकवचन", "द्विवचन", "बहुवचन"];

// Automation Loop generating 21 questions dynamically for every noun
for (let key in nounMatrices) {
    let item = nounMatrices[key];
    for (let vIdx = 0; vIdx < 7; vIdx++) {
        for (let wIdx = 0; wIdx < 3; wIdx++) {
            let correctForm = item.forms[vIdx][wIdx];
            
            // Collect incorrect options from other forms of the same word
            let wrongPool = new Set();
            item.forms.forEach(row => row.forEach(form => { if(form !== correctForm) wrongPool.add(form); }));
            let wrongOptions = Array.from(wrongPool).sort(() => 0.5 - Math.random()).slice(0, 3);
            
            let options = [{ t: correctForm, c: true, r: `साधु! एतत् ${vibhaktis[vIdx]}विभक्तेः ${vacanas[wIdx]}रूपम् अस्ति।` }];
            wrongOptions.forEach(w => {
                options.push({ t: w, c: false, r: `असमीचीनम्। एतत् रूपं न भवति।` });
            });
            options.sort(() => 0.5 - Math.random()); // Randomize options distribution

            window.nounQuestions.push({
                t: key,
                q: `${item.stem}शब्दस्य ${vibhaktis[vIdx]}विभक्तेः ${vacanas[wIdx]}रूपं किम्?`,
                h: item.hint,
                o: options
            });
        }
    }
}
