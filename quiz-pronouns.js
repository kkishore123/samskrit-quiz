const vLabels = ["प्रथमा", "द्वितीया", "तृतीया", "चतुर्थी", "पञ्चमी", "षष्ठी", "सप्तमी"];
const wLabels = ["एकवचन", "द्विवचन", "बहुवचन"];

// Automation Loop generating 21 questions dynamically for every pronoun configuration
for (let key in pronounMatrices) {
    let item = pronounMatrices[key];
    for (let vIdx = 0; vIdx < 7; vIdx++) {
        for (let wIdx = 0; wIdx < 3; wIdx++) {
            let correctForm = item.forms[vIdx][wIdx];
            
            // Collect incorrect options safely from other forms of the same word matrix
            let wrongPool = new Set();
            item.forms.forEach(row => row.forEach(form => { if(form !== correctForm) wrongPool.add(form); }));
            let wrongOptions = Array.from(wrongPool).sort(() => 0.5 - Math.random()).slice(0, 3);
            
            let options = [{ t: correctForm, c: true, r: `साधु! एतत् ${vLabels[vIdx]}विभक्तेः ${wLabels[wIdx]}रूपम् अस्ति।` }];
            wrongOptions.forEach(w => {
                options.push({ t: w, c: false, r: `असमीचीनम्। एतत् रूपं न भवति।` });
            });
            options.sort(() => 0.5 - Math.random());

            window.pronounQuestions.push({
                t: key,
                q: `${item.stem}शब्दस्य ${vLabels[vIdx]}विभक्तेः ${wLabels[wIdx]}रूपं किम्?`,
                h: item.hint,
                o: options
            });
        }
    }
}
